    private void login(String email, String password) {
        Map<String, String> cred = new HashMap<String, String>();
        cred.put("username", email);
        cred.put("password", password);
        Call<JsonObject> call = apiService.login(cred);
        call.enqueue(new Callback<JsonObject>() {
            @Override
            public void onResponse(Call<JsonObject> call, Response<JsonObject> response) {
                if(response.code()==200) {
                    Auth.setToken(getApplicationContext(), response.body().getAsJsonPrimitive("token").getAsString());
                    Initializers.getInstance().initUser(getApplicationContext(), new Initializers.InitUserResponse() {
                        @Override
                        public void hasFinishedWithResponse(User user) {
                            String token = Notification.getDeviceToken(getApplicationContext());
                            if(token != null){
                                sendNotificationTokenToServer(token);
                            }
                            Intent intent = new Intent(getApplicationContext(), ShipmentsActivity.class);
                            startActivity(intent);
                            LoginActivity.this.finish();
                        }

                        @Override
                        public void hasFailed(Call<User> call, Throwable throwable, Response<User> response) {
                            showProgress(false);
                            ErrorHandler.getInstance().processError(throwable, "", getApplicationContext(), new ErrorCallback() {
                                @Override
                                public void alertDismissed(Constants.ERR_TYPE errorType) {

                                }
                            });
                        }
                    });
                } else {
                    mPasswordView.setError(getString(R.string.error_incorrect_password));
                    mPasswordView.requestFocus();
                    showProgress(false);
                }
            }

            @Override
            public void onFailure(Call<JsonObject> call, Throwable t) {
                showProgress(false);
                ErrorHandler.getInstance().processError(t, "", getApplicationContext(), new ErrorCallback() {
                    @Override
                    public void alertDismissed(Constants.ERR_TYPE errorType) {

                    }
                });
            }
        });
    }

    private void sendNotificationTokenToServer(String token) {
        JsonObject parameters = new JsonObject();
        parameters.addProperty("platform_type", 3);
        parameters.addProperty("identifier", token);
        Call<JsonObject> call = RestClient.getInstance(getApplicationContext()).apiServiceAuthenticated().postPlatform(parameters);
        call.enqueue(new Callback<JsonObject>() {
            @Override
            public void onResponse(Call<JsonObject> call, Response<JsonObject> response) {

            }

            @Override
            public void onFailure(Call<JsonObject> call, Throwable t) {

            }
        });
    }
